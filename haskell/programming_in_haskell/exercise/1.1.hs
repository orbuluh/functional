-- Give another possible calculation for the result of
-- double (double 2)

double (double 2)
= (double 2) + (double 2)
= (double 2) + 2 + 2
= (double 2) + 4
= (2 + 2) + 4
= 4 + 4
= 8

double (double 2)
= double (2 + 2)
= (2 + 2) + (2 + 2)
= 4 + 4
= 8

double (double 2)
= double (2 + 2)
= double (4)
= 4 + 4
= 8